public class HotwhellsPista {
    private int ID;
    private String nome;
    private int lote;
    private int cor;
    private int peças;

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getLote() {
        return lote;
    }

    public void setLote(int lote) {
        this.lote = lote;
    }

    public int getCor() {
        return cor;
    }

    public void setCor(int cor) {
        this.cor = cor;
    }

    public int getPeças() {
        return peças;
    }

    public void setPeças(int peças) {
        this.peças = peças;
    }
}
